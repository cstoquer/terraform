function Point(x, y) {
	this.x = x;
	this.y = y;
}

//▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄
Point.prototype.distance = function (point) {
	var x = this.x - point.x;
	var y = this.y - point.y;
	return Math.sqrt(x * x + y * y);
}